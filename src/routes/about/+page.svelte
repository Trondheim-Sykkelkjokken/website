<h1>About</h1>
<p>
	Trondheim Bike Kitchen is a non-profit bike workshop. Here members get access to tools and
	guidance in how to repair their own bikes. "Bike kitchen" is an established concept for sharing
	knowledge and resources on bike repair and similar initiatives exist in countries all over the
	world. Our goal is to be a community for sharing knowledge about repairing and inspiration for
	cycling. We also wish to contribute to reduce resource consumption by giving bikes a longer life
	through repairs and good maintenance.
</p>

